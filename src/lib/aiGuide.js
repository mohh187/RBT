// Lightweight keyword search over the guide. Returns the best-matching sections.
export function searchGuide(query, max = 3) {
  const q = String(query || '').toLowerCase().trim()
  if (!q) return []
  const words = q.split(/\s+/).filter((w) => w.length > 1)
  const scored = SYSTEM_GUIDE.map((s) => {
    let score = 0
    for (const w of words) {
      if (s.topic.toLowerCase().includes(w)) score += 3
      if (s.keys.some((k) => k.includes(w) || w.includes(k))) score += 2
      if (s.text.toLowerCase().includes(w)) score += 1
    }
    return { s, score }
  }).filter((x) => x.score > 0).sort((a, b) => b.score - a.score)
  return scored.slice(0, max).map((x) => ({ topic: x.s.topic, guide: x.s.text }))
}
