// Longest-prefix route matching: '/admin' exact-only so it doesn't swallow children.
export function guideForPath(pathname) {
  const p = String(pathname || '')
  let best = null
  for (const g of PAGE_GUIDES) {
    if (g.exact) { if (p === g.path) return g; continue }
    if (p === g.path || p.startsWith(g.path + '/')) {
      if (!best || g.path.length > best.path.length) best = g
    }
  }
  return best
}

// Keyword lookup for the assistant's explain_page tool.
export function findGuides(query, max = 2) {
  const q = String(query || '').toLowerCase().trim()
  if (!q) return []
  const words = q.split(/\s+/).filter((w) => w.length > 1)
  const scored = PAGE_GUIDES.map((g) => {
    const hay = (g.title + ' ' + g.key + ' ' + g.path + ' ' + g.intro).toLowerCase()
    let score = 0
    for (const w of words) if (hay.includes(w)) score += 2
    if (g.title.toLowerCase() === q) score += 5
    return { g, score }
  }).filter((x) => x.score > 0).sort((a, b) => b.score - a.score)
  return scored.slice(0, max).map((x) => x.g)
}
