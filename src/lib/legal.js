export const LEGAL_ORDER = ['terms', 'privacy', 'refund', 'aup']
// The docs a venue must accept to onboard (bumping a version re-prompts consent).
export const REQUIRED_CONSENT = ['terms', 'privacy']
export const CONSENT_VERSION = '2026-07-04'

// ---- Firestore-backed overrides (edited from the platform console) ----
const legalRef = () => doc(db, 'platformConfig', 'legal')

// Merge a published override (if any) over the built-in default.
export function mergeLegal(published, id) {
  const base = LEGAL_DEFAULTS[id]
  const over = published && published[id]
  if (!over) return base
  return { ...base, ...over, sections: over.sections || base.sections }
}

export async function loadPublishedLegal() {
  try {
    const snap = await getDoc(legalRef())
    return snap.exists() ? snap.data() : {}
  } catch {
    return {}
  }
}
export function watchPublishedLegal(cb) {
  return onSnapshot(legalRef(), (s) => cb(s.exists() ? s.data() : {}), () => cb({}))
}
export async function savePublishedLegalDoc(id, docData) {
  await setDoc(legalRef(), { [id]: { ...docData, updated: docData.updated || new Date().toISOString().slice(0, 10) } }, { merge: true })
}

// ---- consent recording (ties into platformConsent) ----
export async function recordLegalConsent({ tenantId, uid, byName, docs, version }) {
  try {
    await addDoc(collection(db, 'platformConsent'), {
      tenantId: tenantId || null,
      uid: uid || null,
      byName: byName || '',
      kind: 'legal-acceptance',
      docs: docs || REQUIRED_CONSENT,
      version: version || CONSENT_VERSION,
      at: serverTimestamp(),
    })
  } catch {
    /* non-fatal — consent is best-effort logged */
  }
}
